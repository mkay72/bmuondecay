c       - - - - - - - - - - - - - - - - - - - - - - - - -
        implicit real*8 (A-H,O-Z)
c       - - - - - - - - - - - - - - - - - - - - - - - - -
        parameter (pi=3.1415926535897932385d0)
c       - - - - - - - - - - - - - - - - - - - - - - - - -
        dn=1+(pi*a/c)**2-6d0*((a/c)**3)*SK(3,-c/a)
        
